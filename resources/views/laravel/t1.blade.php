<p>プログラミングを行う開発環境として、AWS Cloud9を使います。</p>

<h4>AWS Cloud9とは</h4>

<p>AWS Cloud9は、アプリケーションの開発やデータベースなどをクラウド環境で利用できるサービスです。<br>
ブラウザ上で動くため、PCに依存することなく開発環境を準備することができます。<br>
また、環境を0から構築する必要がなく、素早く開発環境を構築できるので、すぐに開発に取り組むことができます。</p>

<div class="alert alert-warning">
以前は「Cloud9」という名称で展開していましたが、Amazon社に運営が引き継がれ、2017年12月1日から「AWS Cloud9」に移行しました。以前のCloud9のアカウントをお持ちであっても、必ず下記の「AWS Cloud9」で新しくご登録ください。
