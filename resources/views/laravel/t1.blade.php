  <li>どういった状態（エラー）であるか</li>
</ul>

<p>どのように質問するかまとめられない場合は「質問がうまくまとまっていませんが」といった前置きをしていただくと、メンターで状況が理解しやすくなります。</p>

<h4>Slackで質問する際の注意点</h4>

<p><strong>メンターは受講生の正しいスキル定着のため、質問にそのまま回答するのではなく、問題解決や気づきにつながるような回答を心がけています。</strong></p>

<p>そのため、質問の際は下記のことを守っていただくようお願いします。  <br>
解決がスムーズになるだけでなく、正しい理解にもつながります。</p>

<ul>
  <li><strong>まずは自分で調べましょう</strong></li>
  <li><strong>調べてみて、どこがわからなかったのか伝えてみましょう</strong></li>
  <li><strong>エラーが出た場合は、原因について仮説を立ててまずは自分で試してみましょう</strong></li>
  <li><strong>それでもだめな場合は、試したことを伝えてみましょう</strong></li>
  <li><strong>具体的なレッスンおよびチャプター・セクションの場所を伝えましょう</strong>（例：レッスン0の8.2 質問方法(Slack)の操作方法がわかりません）</li>
</ul>
