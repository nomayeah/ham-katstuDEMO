<h4>（参考）swap領域の設定</h4>

<p>今後、学習を進める中で <code>Your environment is running out of quota. Please make some free space.</code> という警告がCloud9の画面右上に出るかもしれません。その際は、上記のメモリ解放のコマンドを実行すれば解消されるかと思います。</p>

<p>別の手段としてswap領域を設定する方法もあります。swap領域は、HDDの中に確保された「メモリの一部として機能させる領域」です。あまりにも多発する場合は、下記の以下のコマンドを一つ一つ実行してswap領域を設定してみてください（このコマンドの意味を理解するのは難しいので、単純に1行ずつ実行してもらえれば大丈夫です）。</p>

<div class="language-sh highlighter-coderay"><div class="CodeRay">
  <div class="code"><pre>$ sudo dd if=/dev/zero of=/var/swap.1 bs=1M count=1024
$ sudo chmod 600 /var/swap.1
$ sudo mkswap /var/swap.1
$ sudo swapon /var/swap.1
$ sudo cp -p /etc/fstab /etc/fstab.ORG
$ sudo sh -c "echo '/var/swap.1 swap swap defaults 0 0' &gt;&gt; /etc/fstab"
</pre></div>
</div>
</div>

<p><img src="https://techacademy.s3.amazonaws.com/bootcamp/laravel/orientation/install_php_10.png" alt="install_php_10.png"></p>

<div class="alert alert-warning">
上記の設定を間違えた場合や <code>count=</code> の数値を変更したい場合は、以下のコマンドを順に実行してください（ただし <code>count=1024</code> より大きくするとHDD容量が不足するかもしれないので <code>count</code> の値は <code>1024</code> を推奨します）。<br>
<br>
<pre>sudo swapoff /var/swap.1
sudo rm /var/swap.1
sudo dd if=/dev/zero of=/var/swap.1 bs=1M count=1024
sudo chmod 600 /var/swap.1
sudo mkswap /var/swap.1
sudo swapon /var/swap.1
</pre>
</div>

